package E3_Umano;

public class Polmoni {
    private int numeroPolmoni;
    private String salutePolmoni;

    public Polmoni(int numeroPolmoni, String salutePolmoni) {
        this.numeroPolmoni = numeroPolmoni;
        this.salutePolmoni = salutePolmoni;
    }

    public int getNumeroPolmoni() {
        return numeroPolmoni;
    }

    public String getSalutePolmoni() {
        return salutePolmoni;
    }
}
